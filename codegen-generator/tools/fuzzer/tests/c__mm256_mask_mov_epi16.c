#include <immintrin.h>
#include <stdio.h>
#include <stdint.h>


void hex_out(const uint8_t * buf, ssize_t sz) {
    for (ssize_t i = sz - 1; i >= 0; --i) {
        printf("%02x", buf[i]);
    }
    printf("\n");
}


int main() {
__m256i src = _mm256_set_epi16(0x1f1e,0x1d1c,0x1b1a,0x1918,0x1716,0x1514,0x1312,0x1110,0x0f0e,0x0d0c,0x0b0a,0x0908,0x0706,0x0504,0x0302,0x0100);
__mmask16 k = _cvtu32_mask16(0x2120);
__m256i a = _mm256_set_epi16(0x4140,0x3f3e,0x3d3c,0x3b3a,0x3938,0x3736,0x3534,0x3332,0x3130,0x2f2e,0x2d2c,0x2b2a,0x2928,0x2726,0x2524,0x2322);
  __m256i ret = _mm256_mask_mov_epi16(src, k, a);
  hex_out(&ret, 32);
  return 0;
}